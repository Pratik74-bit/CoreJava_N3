package org.tnsif.looping;

public class DoWhileDemo {

	public static void main(String[] args) {
		
		
		int n = 20;
		
		//Example of Do while loop
		do {
			System.out.println(n + " is less than 30");
			n++;
		} while (n < 30); //condition is value should be less than 30

	}

}
